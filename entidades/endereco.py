class Endereco:

    def __init__(self, numero=0, rua="", complemento="", bairro="", cep="", cidade="", estado="", pais=""):
        self.numero = numero
        self.rua = rua
        self.complemento = complemento
        self.bairro = bairro
        self.cep = cep
        self.cidade = cidade
        self.estado = estado
        self.pais = pais


